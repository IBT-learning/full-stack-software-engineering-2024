import { mongoose } from "../db.js";

const Recipe = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    ingredients: [
      {
        type: String,
        required: true,
      },
    ],
    instructions: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model("recipe", Recipe);
